/**
 * DOCX·평문 내보내기용: 모델이 한 줄로 붙인 해설을 읽기 좋게 줄바꿈만 보강한다.
 * (KaTeX 미리보기·원문 LaTeX에는 적용하지 않는다.)
 */

export function loosenExplanationParagraphs(text: string): string {
  let s = text.replace(/\r\n/g, "\n").trim();
  if (!s) return s;

  // 한글 문장: 마침표+공백+한글
  s = s.replace(/([가-힣]{2,})\.\s+(?=[가-힣])/g, "$1.\n");

  // 종결 어미 뒤 단계/접속
  s = s.replace(
    /(이다|니다|습니다|된다|한다|해요|하죠)\.\s+(따라서|이제|그러므로|즉|한편|또한|먼저|그래서|이때)(?=[\s가-힣])/g,
    "$1.\n$2",
  );

  // 숫자 결론 뒤 한글 단계 (3.14 오인 완화: 뒤 토큰 제한)
  s = s.replace(
    /(\d)\.\s+(따라서|이제|그러므로|즉|한편|또한|먼저|그래서|이때|주어진|구하면|따라)(?=[가-힣])/g,
    "$1.\n$2",
  );

  // 문장 중 접속(먼저는 제외 — '을 먼저' 등 오탐)
  s = s.replace(/([^\n])\s+(따라서|그러므로|한편|또한|즉,|즉\s)(?=[\s가-힣])/g, "$1\n$2");

  s = s.replace(/\n{3,}/g, "\n\n");
  return s.trim();
}
